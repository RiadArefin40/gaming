function convertToCSV(text) {
  const lines = text.trim().split("\n");

  const rows = lines.map(line => {
    const parts = line.trim().split(/\s+/);
    const uid = parts.pop(); // last value
    const name = parts.join(" "); // rest is name

    return `"${name}","${uid}"`;
  });

  return rows.join("\n");
}


