function attachUIDFromCSV(gameArray, csv) {
  const map = {};

  csv
    .trim()
    .split("\n")
    .forEach(line => {
      const [rawName, rawUid] = line.split(",");

      const name = rawName.replace(/"/g, "").trim().toLowerCase();
      const uid = rawUid.replace(/"/g, "").trim();

      map[name] = uid;
    });

  return gameArray.map(item => {
    const normalizedName = item.name
      .replace(/^EVOLUTION\s+/i, "") // remove EVOLUTION prefix
      .trim()
      .toLowerCase();

    return {
      ...item,
      uid: map[normalizedName] || null
    };
  });
}



